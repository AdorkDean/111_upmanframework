function writeSelect(selectName,formName,formValue){

  document.write('<select name="'+formName+'">'); 
  writeOption(selectName);
  document.write('</select>');

  
  var selObj = document.all(formName);
  for(var i = 0; i < selObj.length; i++)
  {
  	if(selObj.options[i].value == formValue)
  	{
  		selObj.options[i].selected = true;
		break;
  	}
  }
}
